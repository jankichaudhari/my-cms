<h1>Joining a group</h1>

<div id="joinGroup">

<div class="expBlockBg">
<p><span class="expBlockTitle" id="y1">Choose your asset</span></p>
<p><span class="expBlockDesc" id="expBlockDescy1">
There are a number of considerations when selecting your asset.  It&lsquo;s worth taking some time to find one that suits your lifestyle and you needs.  Look at the group as much as the particular asset.   Some considerations are listed below.
<br/><br/>
&nbsp;&bull;&nbsp;<span style="color:#FFF">The asset type</span> – There will be many variations of similar assets available and inevitably, as with most purchases, is a compromise.  Be clear of what you want out of it.<br/>
&nbsp;&bull;&nbsp;<span style="color:#FFF">Location</span> – The asset should be kept in an accessible location if you are to take advantage and use it to its full potential.  Ideally local enough to be able to use on impulse.<br/>
&nbsp;&bull;&nbsp;<span style="color:#FFF">Storage and Access</span> – Consider how easy it is to get access (own key/shared key etc).  Ensure it is safe and offers you enough independence to use at your convenience.  Are there suitable facilities for cleaning and maintaining the asset where it is stored?<br/>
&nbsp;&bull;&nbsp;<span style="color:#FFF">Use</span> – Enquire if the asset is used by anyone external to the group for any reason, training for example.<br/>
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="y2">Meet the Group Members</span></p>
<p><span class="expBlockDesc" id="expBlockDescy2">
Meeting members other than the seller will give you a different perspective on the group&lsquo;s dynamics, general sentiment and atmosphere.  Be sure they fit you as much as you fit them. 
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="y3">Access the booking system</span></p>
<p><span class="expBlockDesc" id="expBlockDescy3">
Insist on acquiring the seller&lsquo;s user name and password to access the assets online booking system.  Much can be gained from this.  You will get an idea for when the busy times are, who uses it the most and when.
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="y4">Check the Documents</span></p>
<p><span class="expBlockDesc" id="expBlockDescy4">
&nbsp;&bull;&nbsp;Insurance<br/>
&nbsp;&bull;&nbsp;The Groups Agreements<br/>
&nbsp;&bull;&nbsp;Finances&nbsp;&shy;&nbsp;look at the groups financial history to ensure the accounts are healthy.<br/>
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="y5">Have a Test Drive and ask Questions</span></p>
<p><span class="expBlockDesc" id="expBlockDescy5">
As with purchasing any asset, you will gain much by having a test drive in the asset.  The usual precautions apply.  Check it is in good working order and is what you expected and hope for.  Take the opportunity to ask as many questions as you need and don&lsquo;t hesitate to ask &ldquo;uncomfortable&rdquo; questions.
</span></p>
</div>


