<h1>Insurance</h1>

<div class="expBlockBg">
<p>When contacting insurers, be sure to mention Easyshare.  We may have a special rate arranged. </p> 
<br/>
<p>It is important to explain to the insurer that the asset will be in shared ownership.  You must clarify to the insurer how the group is set up.  If, as suggested, the asset is held in company ownership and the users are shareholders this must be explained.  It is more usual, from an insurers perspective, to select one person in the group as the owner and have named drivers.  That however has obvious complications should a claim need to be made.  For this reason, we suggest the company ownership route.  </p>
<br/>
<p>An alternative method is to nominate one person to act on behalf of the group in any negotiations with the insurance company but should a payment be required, the authorization of all members would have to be recieved before a payment was made to a one of the members.  This is an alternative solution should your insurance company not support the insured company method. </p>
<br/>
<p>Remember that for most claims, the insurance company will select a suitable maintenance organization and pay them directly. </p>
<br/>
<p>Some insurers may not provide these types of multiple ownership insurance but our selected partners do.</p>
