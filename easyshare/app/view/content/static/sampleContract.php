<br/><br/>
You may operate outside of these rules only with the express permission of the administrator of the group.  
<br/><br/>
You should amend the rules as you require fitting your requirements.
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="c6">How the Asset is to be Used</span></p>
<p><span class="expBlockDesc" id="expBlockDescc6">
Each member will have their own key / use a communal key stored in _________.  The asset will not be used outside the booking times as registered on the online calendar.  If the user is unable (for any reason) to return the vehicle within the booked time and it is going to infringe another members booking, the affected member should be notified as soon as practicable.  Every effort should be made to operate within the booked times.
<br/><br/>
Each member must operate the asset within the requirements of the asset&lsquo;s insurance.  Any person not covered by the insurance / who is not a member may not drive / pilot the asset.
<br/><br/>
Before each use, the member should<br/>
&nbsp;&bull;&nbsp;Ensure the asset is free from damage<br/>
&nbsp;&bull;&nbsp;Clean<br/>
&nbsp;&bull;&nbsp;etc<br/>
<br/><br/>

After each use the asset should be returned in the following condition:<br/>
&nbsp;&bull;&nbsp;Full of fuel / empty of fuel<br/>
&nbsp;&bull;&nbsp;Clean, (explain here exactly the standard of cleaning required e.g. external, internal, empty the fridge, hovered etc).<br/>
&nbsp;&bull;&nbsp;Parked in the specified position<br/>
&nbsp;&bull;&nbsp;Free of damage<br/>
&nbsp;&bull;&nbsp;etc.<br/>
<br/><br/>

Try to be as specific as possible in this section and ensure that each aspect is covered in detail so as to remove any ambiguity and clarify the expectation of each member.  As every asset will be different, stored and accessed in different ways and has differing requirements, it is very hard to complete this section for you.  Give it some consideration.
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="c7">Asset Maintenance</span></p>
<p><span class="expBlockDesc" id="expBlockDescc7">
The asset&lsquo;s requirement for maintenance will take precedence over any bookings in the booking sheet.  Planned maintenance will be added (by the appropriate member) to the booking sheets as far in advance as is practical to minimize the effect on member bookings.  Every effort will be made to ensure that planned maintenance is carried out when the demand for the asset is at it&lsquo;s lowest (out of season).
<br/><br/>
If a member notices the asset is damaged in any way or requires any maintenance, the administrator must be informed immediately.  The administrator will assess which member is responsible for the damage and contact them accordingly.  If the damage is such that maintenance is required either immediately or in the future, the administrator will contact those whose bookings will be affected due to the maintenance action.
<br/><br/>
The person liable for the damage must take responsibility.  If the damage is considered to be &ldquo;wear and tear&rdquo; the group may decide to add it to the next maintenance bill covered by the group as a whole.   If it requires repair and the cost is over ______ pounds, a claim will be made from the insurer and any excess invoiced to the liable member.
<br/><br/>
This is a method of arranging the scheduled and unscheduled maintenance requirements.  Amend it to suit the circumstances of the group.
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="c8">The Dynamics of the Group</span></p>
<p><span class="expBlockDesc" id="expBlockDescc8">
All members will be invited to attend a social meeting once every __ months.  The purpose is to discuss the finances of the group and any associated issues.  It is also an opportunity to discuss any upgrades required or issues that need attention.  All changes will be decided by vote and a majority will pass the action.
<br/><br/>
If a member has been noticed to be consistently operating outside of the agreement outlined in this document or in a manner that is deemed inappropriate by a majority of the group, the group reserves the right to restrict the member&lsquo;s access to the asset until a meeting with the member and the group has taken place.  The subject will then be discussed and an agreement on how to proceed finalised.  The member will receive a yellow card after such a meeting, which is stored on their profile.  After the member has accumulated __ yellow cards, the group has the option to place the offending members share up for sale.  Once a new member has been recruited and the share sold for a price decided by the group, the costs will be recouped to the group accounts from the sale and the remaining monies paid to the outgoing member.
<br/><br/>
It would be foolish not to consider the possibility of having a member who may be a little troublesome.  The group should always run on mutual respect and the members selected to be cohesive.  This is not always possible however and on occasion action maybe required.  The above is a possible way of moving forward.  Consider your options and adapt it to suit your asset.
</span></p>
</div>

