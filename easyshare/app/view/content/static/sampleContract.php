<h1>Sample Group Contract</h1>

<p>This agreement relates to all member&lsquo;s of the group listed here / member&lsquo;s of the named LLP /  shareholders listed with the company _________ Ltd </p>

<p>The contents of this agreement may only be changed with the signed agreement of all members.</p>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="c1">New Members</span></p>
<p><span class="expBlockDesc" id="expBlockDescc1">
New members must be aged between __ and __ years of age.  They require prior/no prior experience to become members and the experience level is set at _____ hours/miles/etc.  They should have the following qualification _______.  If the qualification or experience levels are not met, the asset may/may not be used for training or instruction to reach the level stipulated.  
<br/><br/>
Add any other requirements, as you need.  The limits you set (age for example) may have an effect on your insurance premiums and is worth investigating, but the higher the limits, the more difficult it becomes to find suitable partners. 
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="c2">Group Entry and Exit</span></p>
<p><span class="expBlockDesc" id="expBlockDescc2">
Members should base the sale of their share on a market valuation of the asset.  The member may sell for less than the market value but that will be at their own expense.  A member may advertise their share in the asset though any methods provided it does not bring the group into disrepute or lower the overall value of the asset.
<br/><br/>
The seller of the share should ensure the new buyer meets all the criteria listed above under &ldquo;new members&rdquo;.  The seller may show the asset to the new buyer and offer supervised access or use for familiarization and exposure purposes.  The buyer may not use the asset alone.  At all times the insurance criteria for the asset must be maintained.  
<br/><br/>
Once the buyer has expressed an interest in completing the sale, the seller should arrange a meeting with another group member and a &ldquo;test drive&rdquo; arranged.  The purpose of this is for familiarization of group&lsquo;s etiquette and procedures as well as helpful advice for the new member.  The new member should also be deemed to be appropriate and suitable for the group&lsquo;s requirements.  Any member of the group may be tasked with this / the group administrator must be tasked with this / both the administrator and a group member must be tasked with this.  If concern is expressed about the new member, an alternative member may be asked to complete a &ldquo;test drive&rdquo; and state their judgment.  If both are unhappy, the sale may not be processed.
<br/><br/>
Once complete, the details of the buyer shall be passed on to the administrator of the group to complete all the financial and identity details (covered in ID Requirements below).  All company documents will be amended to include the new member.
<br/><br/>
The seller is fully responsible for the asset until the completion of the sale and acceptance of the new member by the group.  The financial transaction should be completed privately and independently.
<br/><br/>
These conditions are set to protect the asset and ensure the group as a whole are pleased with any new member that wants to enter the group.  Shared ownership relies on mutual respect, a good friendly atmosphere and financial security.  New members should be vetted and fit your groups specifications.  Change the wording if required to suit your requirements and the asset.
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="c3">ID Requirements</span></p>
<p><span class="expBlockDesc" id="expBlockDescc3">
Each member must provide to the administrator a copy of their passport, relevant license, a bank statement with home address and a credit check from Experian.co.uk.  The credit check must have been completed within the last year.  If the credit check shows a poor reading, the member will be declined / the member may be further investigated and voted on.  All original documents must be viewed with receipt of the photocopies.  All the members&lsquo; copied documents will be kept in a safe place and will not be used for any purpose other than that related to the running of the group.
<br/><br/>
The group administrator will provide the same documents to another member of the group. And show new members should they wish to see them.  
<br/><br/>
If the group is formed of people who have only met in relation to the asset, it is wise to ensure you have verified all their identity details.  The varying values and types of asset to which this contract can be applied, will mean some parts of the above are more relevant than others.  The Experian credit check is free at the moment and may be worth considering as it gives an indication of how credit worthy a member is.
</span></p>
</div>

<div class="expBlockBg">
<p><span class="expBlockTitle" id="c4">Finance</span></p>
<p><span class="expBlockDesc" id="expBlockDescc4">
The value of the asset as of the ____(Date)______ is &pound;________.
<br/><br/>
The agreed monthly payment will be taken on the ___ of the month and the amount is &pound;_________.
<br/><br/>
The usage charge for the asset will be taken on the ____ of the month / week and will be set at&nbsp;&shy;&nbsp;&pound;_______ per _______.
<br/><br/>
On receipt of the bill, payment must be made within ____ days.
<br/><br/>
The money will be kept in the group bank account, which may be accessed by members _______________.  The removal of money requires signatures of 2 members.
<br/><br/>
Any money removed from the account will be registered on Easyshare&lsquo;s online Asset Management software to keep all the accounts transparent.  The accounts will be available for any member to view at any reasonably requested time.
<br/><br/>
As a group we will meet every 3 months to discuss the finances and keep everyone up to date with developments and general housekeeping issues related to the asset.  All members must attend unless they have notified the administrator.   
<br/><br/>
Should a member be unable to upkeep his or her financial commitments to the group, the group reserve the right to restrict access to the asset.  After the member in question becomes ____ months in arrears, the group may place the offending members share up for sale.  Once a new member has been recruited and the share sold for a price decided by the group, the costs will be recouped to the group accounts from the sale and the remaining monies paid to the outgoing member.
<br/><br/>

Keep this page separate to the rest of the contract and update it every time the asset is valued and if any prices are changed at any point.  It can simply be e-mailed.
</span></p>
</div>

<div class="expBlockBg">
