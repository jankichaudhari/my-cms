<p><span class="expBlockDesc" id="expBlockDescc5">
The asset will be run using a rule based system / an allocation rota.  The specific allocation allowances are listed below.
<br/><br/>
<span style="color:#FFF;">Rule based system</span>
<br/><br/>
The maximum number each member may have on the online calendar at any one point are:
<br/><br/>
